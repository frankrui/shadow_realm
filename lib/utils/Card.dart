class Card {

  String id;
  String name;
  String type;
  String desc;
  String race;
  String archetype;

  Card(String id, String name, String type, String desc, String race, String archetype) {
    this.id = id;
    this.name = name;
    this.type = type;
    this.desc = desc;
    this.race = race;
    this.archetype = archetype;
  }
}